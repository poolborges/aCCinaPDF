    private final long WAIT_TIME = 2000;
    /**
     * Creates new form SplashScreen
     */
    public SplashScreen() {
        initComponents();
        setLocationRelativeTo(null);
        
        final Icon splashScreen = new ImageIcon(SplashScreen.class.getResource("/image/splashScreen.jpg"));
        lbBackground.setIcon(splashScreen);
        
        final List<Image> icons = new ArrayList<>();
        icons.add(new ImageIcon(getClass().getResource("/image/aCCinaPDF_logo_icon32.png")).getImage());
        this.setIconImages(icons);
        
        final MainWindow mw = new MainWindow();
        mw.setLocationRelativeTo(null);
        
        new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    Thread.sleep(WAIT_TIME);
                    dispose();
                    mw.setVisible(true);
                } catch (InterruptedException ex) {
                    controller.Logger.getLogger().addEntry(ex);
                }
            }
        }).start();
    }

